#include <bits/stdc++.h>

using namespace std;


int main(){

    // and (&) operation:
    int a = 3 & 7;

    // or (|) operation:
    int b = 3 | 7;

    // xor (^) operation:
    int c = 3 ^ 7;

    // not (~) operation:
    int d = ~7;

    // Left Shift operation (<<)
    int e = (3 << 7); // this is equivalent to 3 * pow(2,7)

    // Right Shift operation (>>)
    int f = (3 >> 7); // this is equivalent to 3 / pow(2,7)

}